cars = ['bmw', 'audi', 'toyota', 'subaru']
print(cars)

cars.reverse() # вывод списка в обратном порядке
print(cars)

cars.sort() # сортировка по алфавиту
print(cars)

cars.sort(reverse=True) # сортировка по алфавиту в обратном порядке
print(cars)

print(sorted(cars)) # временная сортировка списка

print(len(cars)) # len - длина списка